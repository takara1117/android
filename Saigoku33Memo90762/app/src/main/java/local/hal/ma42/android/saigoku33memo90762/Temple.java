package local.hal.ma42.android.saigoku33memo90762;

public class Temple
{    
    private int _id;

    private String _name;

    private String _honzon;

    private String _shushi;

    private String _address;

    private String _url;

    private String _note;

    //アクセサメソッド
    public int getId()
    {
        return _id;
    }

    public void setId(int id)
    {
        _id = id;
    }

    public String getName()
    {
        return _name;
    }

    public void setName(String name)
    {
        _name = name;
    }

    public String getHonzon()
    {
        return _honzon;
    }

    public void setHonzon(String honzon)
    {
        _honzon = honzon;
    }

    public String getShushi()
    {
        return _shushi;
    }

    public void setShushi(String shushi)
    {
        _shushi = shushi;
    }

    public String getAddress()
    {
        return _address;
    }

    public void setAddress(String address)
    {
        _address = address;
    }

    public String getUrl()
    {
        return _url;
    }

    public void setUrl(String url)
    {
        _url = url;
    }

    public String getNote()
    {
        return _note;
    }

    public void setNote(String note)
    {
        _note = note;
    }
}
